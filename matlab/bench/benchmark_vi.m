function rows = benchmark_vi()
%BENCHMARK_VI Speed/accuracy comparison: paper reference vs FPGA-mimic kernel.
%
%   Runs each case from bench_cases() until convergence on both implementations,
%   then prints a Markdown summary to stdout and writes a CSV under
%   matlab/bench/results/benchmark_<timestamp>.csv.

    root_dir = fileparts(mfilename('fullpath'));
    repo_matlab = fileparts(root_dir);
    addpath(fullfile(repo_matlab, 'src'));
    addpath(fullfile(repo_matlab, 'test'));

    p = vi_params();
    max_sweeps = 200;
    threshold  = 0;
    transitions = gen_transitions('paper_mc');

    cases = bench_cases();
    rows = repmat(empty_row(), 1, numel(cases));

    fprintf('Running %d cases...\n', numel(cases));
    for k = 1:numel(cases)
        c = cases(k);
        fprintf('  [%2d/%2d] %-14s ', k, numel(cases), c.name);

        [value0, penalty, ~, ~, goal_mask] = gen_test_map( ...
            c.map_x, c.map_y, c.type, c.opts);

        % Warm-up (discard): one reference sweep + one mimic pair.
        warmup(value0, penalty, goal_mask, transitions, c.map_x, c.map_y);

        % Paper reference (oracle)
        t0 = tic;
        [v_ref, a_ref, sw_ref, d_ref] = vi_full_reference( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, threshold, max_sweeps);
        t_ref_ms = toc(t0) * 1000;

        % FPGA-mimic streaming kernel
        t0 = tic;
        [v_fpga, sw_fpga, d_fpga] = run_fpga_mimic( ...
            value0, penalty, goal_mask, transitions, ...
            c.map_x, c.map_y, max_sweeps);
        t_fpga_ms = toc(t0) * 1000;

        a_fpga = compute_action_table_reference(v_fpga, penalty, ...
            goal_mask, transitions, c.map_x, c.map_y);

        m = compute_bench_metrics(v_ref, v_fpga, a_ref, a_fpga, ...
            goal_mask, penalty, p);

        rows(k) = struct( ...
            'name',                c.name, ...
            'map_x',               c.map_x, ...
            'map_y',               c.map_y, ...
            'type',                c.type, ...
            'ref_sweeps',          sw_ref, ...
            'ref_total_ms',        t_ref_ms, ...
            'ref_per_sweep_ms',    t_ref_ms / max(sw_ref, 1), ...
            'ref_final_delta',     d_ref, ...
            'ref_converged',       d_ref <= threshold, ...
            'fpga_sweeps',         sw_fpga, ...
            'fpga_total_ms',       t_fpga_ms, ...
            'fpga_per_sweep_ms',   t_fpga_ms / max(sw_fpga, 1), ...
            'fpga_final_delta',    d_fpga, ...
            'fpga_converged',      d_fpga <= threshold, ...
            'value_mismatch_pct',  m.value_mismatch_pct, ...
            'action_mismatch_pct', m.action_mismatch_pct, ...
            'mean_abs_value_pct',  m.mean_abs_value_pct, ...
            'max_abs_value',       m.max_abs_value);

        fprintf('ref=%6.1fms (%3dsw)  fpga=%6.1fms (%3dsw)  vmiss=%.2f%%\n', ...
            t_ref_ms, sw_ref, t_fpga_ms, sw_fpga, m.value_mismatch_pct);
    end

    print_markdown_table(rows);

    results_dir = fullfile(root_dir, 'results');
    if ~exist(results_dir, 'dir'); mkdir(results_dir); end
    csv_path = fullfile(results_dir, ...
        sprintf('benchmark_%s.csv', datestr(now, 'yyyymmdd_HHMMSS'))); %#ok<TNOW1,DATST>
    write_csv(rows, csv_path);
    fprintf('\nCSV written to %s\n', csv_path);
end

% ---------------------------------------------------------------------------

function row = empty_row()
    row = struct( ...
        'name', '', 'map_x', 0, 'map_y', 0, 'type', '', ...
        'ref_sweeps', 0, 'ref_total_ms', 0, 'ref_per_sweep_ms', 0, ...
        'ref_final_delta', 0, 'ref_converged', false, ...
        'fpga_sweeps', 0, 'fpga_total_ms', 0, 'fpga_per_sweep_ms', 0, ...
        'fpga_final_delta', 0, 'fpga_converged', false, ...
        'value_mismatch_pct', 0, 'action_mismatch_pct', 0, ...
        'mean_abs_value_pct', 0, 'max_abs_value', 0);
end

function warmup(value, penalty, goal_mask, transitions, map_x, map_y)
    % One scratch sweep to amortize MATLAB JIT warm-up. Result discarded.
    vi_full_reference(value, penalty, goal_mask, transitions, map_x, map_y, 0, 1);
    v = value;
    [v, ~] = vi_sweep_stream_algo(v, v, penalty, goal_mask, transitions, map_x, map_y, 0);
    [~, ~] = vi_sweep_stream_algo(v, v, penalty, goal_mask, transitions, map_x, map_y, 1);
end

function [v, sweeps, final_delta] = run_fpga_mimic(value, penalty, goal_mask, ...
    transitions, map_x, map_y, max_sweeps)
    v = value;
    sweeps = 0;
    final_delta = inf;
    for s = 1:max_sweeps
        [v, d0] = vi_sweep_stream_algo(v, v, penalty, goal_mask, ...
            transitions, map_x, map_y, 0);
        [v, d1] = vi_sweep_stream_algo(v, v, penalty, goal_mask, ...
            transitions, map_x, map_y, 1);
        sweeps = s;
        final_delta = max(d0, d1);
        if final_delta == 0
            break;
        end
    end
end

function m = compute_bench_metrics(v_ref, v_fpga, a_ref, a_fpga, goal_mask, penalty, p)
    OB = double(p.PENALTY_OBSTACLE);
    free_mask = ~goal_mask & repmat(penalty ~= OB, [1, 1, p.N_THETA]);
    total = nnz(free_mask);

    if total == 0
        m = struct('value_mismatch_pct', 0, 'action_mismatch_pct', 0, ...
            'mean_abs_value_pct', 0, 'max_abs_value', 0);
        return;
    end

    v_ref_f  = v_ref(free_mask);
    v_fpga_f = v_fpga(free_mask);
    a_ref_f  = a_ref(free_mask);
    a_fpga_f = a_fpga(free_mask);

    denom = max(double(v_ref_f), 1);
    m = struct( ...
        'value_mismatch_pct',  100 * nnz(v_ref_f ~= v_fpga_f) / total, ...
        'action_mismatch_pct', 100 * nnz(a_ref_f ~= a_fpga_f) / total, ...
        'mean_abs_value_pct',  100 * mean(abs(double(v_fpga_f) - double(v_ref_f)) ./ denom), ...
        'max_abs_value',       max(abs(double(v_fpga_f) - double(v_ref_f))));
end

function print_markdown_table(rows)
    fprintf('\n');
    fprintf('| name | size | ref sw | ref ms | ref ms/sw | fpga sw | fpga ms | fpga ms/sw | vmiss%% | amiss%% | meanabs%% | maxabs |\n');
    fprintf('|------|------|-------:|-------:|----------:|--------:|--------:|-----------:|-------:|-------:|---------:|-------:|\n');
    for k = 1:numel(rows)
        r = rows(k);
        fprintf('| %s | %dx%d | %d | %.1f | %.2f | %d | %.1f | %.2f | %.2f | %.2f | %.4f | %.0f |\n', ...
            r.name, r.map_x, r.map_y, ...
            r.ref_sweeps,  r.ref_total_ms,  r.ref_per_sweep_ms, ...
            r.fpga_sweeps, r.fpga_total_ms, r.fpga_per_sweep_ms, ...
            r.value_mismatch_pct, r.action_mismatch_pct, ...
            r.mean_abs_value_pct, r.max_abs_value);
    end
end

function write_csv(rows, path)
    fid = fopen(path, 'w');
    if fid < 0
        error('benchmark_vi:csv', 'cannot open %s for writing', path);
    end
    cleanup = onCleanup(@() fclose(fid)); %#ok<NASGU>

    fprintf(fid, ['name,map_x,map_y,type,' ...
        'ref_sweeps,ref_total_ms,ref_per_sweep_ms,ref_final_delta,ref_converged,' ...
        'fpga_sweeps,fpga_total_ms,fpga_per_sweep_ms,fpga_final_delta,fpga_converged,' ...
        'value_mismatch_pct,action_mismatch_pct,mean_abs_value_pct,max_abs_value\n']);

    for k = 1:numel(rows)
        r = rows(k);
        fprintf(fid, '%s,%d,%d,%s,%d,%.6f,%.6f,%.0f,%d,%d,%.6f,%.6f,%.0f,%d,%.6f,%.6f,%.6f,%.0f\n', ...
            r.name, r.map_x, r.map_y, r.type, ...
            r.ref_sweeps, r.ref_total_ms, r.ref_per_sweep_ms, ...
            r.ref_final_delta, r.ref_converged, ...
            r.fpga_sweeps, r.fpga_total_ms, r.fpga_per_sweep_ms, ...
            r.fpga_final_delta, r.fpga_converged, ...
            r.value_mismatch_pct, r.action_mismatch_pct, ...
            r.mean_abs_value_pct, r.max_abs_value);
    end
end
